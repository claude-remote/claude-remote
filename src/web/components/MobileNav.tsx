import { useLocation, useNavigate } from 'react-router-dom';

interface MobileNavProps {
  sessionId?: string;
}

const tabs = [
  {
    key: 'chat',
    label: 'Chat',
    icon: ChatIcon,
    pathFn: (id?: string) => (id ? `/chat/${id}` : '/sessions'),
  },
  { key: 'sessions', label: 'Sessions', icon: SessionsIcon, pathFn: () => '/sessions' },
  {
    key: 'files',
    label: 'Files',
    icon: FilesIcon,
    pathFn: (id?: string) => (id ? `/files/${id}` : '/sessions'),
  },
  { key: 'settings', label: 'Settings', icon: SettingsIcon, pathFn: () => '/sessions' },
] as const;

export function MobileNav({ sessionId }: MobileNavProps) {
  const location = useLocation();
  const navigate = useNavigate();

  const activeTab = (() => {
    if (location.pathname.startsWith('/chat/')) return 'chat';
    if (location.pathname.startsWith('/files/')) return 'files';
    if (location.pathname === '/sessions') return 'sessions';
    return 'sessions';
  })();

  return (
    <nav
      className="fixed right-0 bottom-0 left-0 z-50 border-t border-gray-800 bg-gray-950/95 backdrop-blur-sm md:hidden"
      style={{ paddingBottom: 'env(safe-area-inset-bottom, 0px)' }}
    >
      <div className="flex items-center justify-around">
        {tabs.map((tab) => {
          const isActive = activeTab === tab.key;
          const Icon = tab.icon;
          return (
            <button
              key={tab.key}
              type="button"
              onClick={() => {
                const path = tab.pathFn(sessionId);
                if (location.pathname !== path) {
                  navigate(path);
                }
              }}
              className={`flex flex-1 flex-col items-center gap-0.5 py-2 text-xs transition-colors ${
                isActive ? 'text-indigo-400' : 'text-gray-500 active:text-gray-300'
              }`}
              aria-label={tab.label}
            >
              <Icon active={isActive} />
              <span>{tab.label}</span>
            </button>
          );
        })}
      </div>
    </nav>
  );
}

